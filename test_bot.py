# ─── ОПРЕДЕЛЕНИЕ ПРОФИЛЯ ЧЕРЕЗ CLAUDE ────────────────────────────────────────

def determine_profile(user_type: int, answers: list) -> int:
    """
    Claude анализирует ответы и возвращает номер профиля: 1, 2 или 3.
    Профиль 1 — реакции опережают / внутренний сторож ведёт.
    Профиль 2 — замечает, но пауза короткая.
    Профиль 3 — пауза есть, хочет углубить.
    """
    test_label = "психолога / терапевта" if user_type == 1 else "человека не из профессии психолога"
    questions = QUESTIONS[user_type]

    answers_text = "\n".join([
        f"Вопрос {i+1}: «{questions[i]['text']}»\nОтвет: «{a}»"
        for i, a in enumerate(answers)
    ])

    prompt = f"""Участник теста: {test_label}

Ответы:
{answers_text}

На основе паттерна ответов определи один из трёх профилей:

Профиль 1 — человек реагирует автоматически, реакции опережают осознание, мало паузы между стимулом и реакцией.
Профиль 2 — человек замечает свои реакции, но пауза между «заметил» и «выбрал иначе» ещё короткая, особенно в интенсивных ситуациях.
Профиль 3 — человек умеет останавливаться и делать выбор осознанно, пауза есть, хочет понять механизм глубже.

Ответь ТОЛЬКО цифрой: 1, 2 или 3. Никакого другого текста."""

    response = claude.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=10,
        messages=[{"role": "user", "content": prompt}]
    )

    raw = response.content[0].text.strip()
    match = re.search(r"[123]", raw)
    return int(match.group()) if match else 2  # fallback — профиль 2


def generate_result(user_type: int, answers: list) -> str:
    profile_num = determine_profile(user_type, answers)
    profile_text = PROFILES[user_type][profile_num]
    return profile_text + CTA_TEXT


# ─── HANDLERS ────────────────────────────────────────────────────────────────

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data.clear()
    await update.message.reply_text(
        INTRO_TEXT,
        parse_mode="Markdown",
        reply_markup=ReplyKeyboardMarkup(TYPE_OPTIONS, resize_keyboard=True, one_time_keyboard=True),
    )
    return SELECT_TYPE


async def select_type(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text
    if "психолог" in text.lower():
        user_type = 1
    elif "не психолог" in text.lower() or "для себя" in text.lower():
        user_type = 2
    else:
        await update.message.reply_text("Пожалуйста, выберите один из вариантов.")
        return SELECT_TYPE

    context.user_data["type"] = user_type
    context.user_data["answers"] = []
    context.user_data["step"] = 0

    return await ask_question(update, context)


async def ask_question(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_type = context.user_data["type"]
    step = context.user_data["step"]
    questions = QUESTIONS[user_type]

    if step >= len(questions):
        return await finish(update, context)

    q = questions[step]
    await update.message.reply_text(
        f"*Вопрос {step + 1} из {len(questions)}*\n\n{q['text']}",
        parse_mode="Markdown",
        reply_markup=ReplyKeyboardMarkup(q["options"], resize_keyboard=True, one_time_keyboard=True),
    )
    return Q1 + step


async def handle_answer(update: Update, context: ContextTypes.DEFAULT_TYPE):
    context.user_data["answers"].append(update.message.text)
    context.user_data["step"] += 1
    return await ask_question(update, context)


async def finish(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Анализирую ваши ответы...\n\nЭто займёт несколько секунд.",
        reply_markup=ReplyKeyboardRemove(),
    )

    user_type = context.user_data["type"]
    answers   = context.user_data["answers"]

    try:
        result = generate_result(user_type, answers)
        await update.message.reply_text(result, parse_mode="Markdown")
    except Exception as e:
        await update.message.reply_text(
            "Что-то пошло не так. Попробуйте начать заново — /start"
        )

    return ConversationHandler.END


async def cancel(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Тест прерван. Чтобы начать заново — /start",
        reply_markup=ReplyKeyboardRemove(),
    )
    return ConversationHandler.END


# ─── ЗАПУСК ──────────────────────────────────────────────────────────────────

def main():
    app = Application.builder().token(V_BOT_TOKEN).build()

    conv = ConversationHandler(
        entry_points=[CommandHandler("start", start)],
        states={
            SELECT_TYPE: [MessageHandler(filters.TEXT & ~filters.COMMAND, select_type)],
            **{Q1 + i: [MessageHandler(filters.TEXT & ~filters.COMMAND, handle_answer)]
               for i in range(7)},
        },
        fallbacks=[CommandHandler("cancel", cancel)],
    )

    app.add_handler(conv)
    print("Бот запущен. Нажмите Ctrl+C для остановки.")
    app.run_polling()


if __name__ == "__main__":
    main()
